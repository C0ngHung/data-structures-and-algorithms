package org.conghung.datastructuresandalgorithms.dsa.queue;

public class Definition {
    // Định nghĩa - Definition:
    // Queue là hàng đợi, xếp hàng và đợi
    // Như xê hàng ở 1 quán cà phê
    // Queue là một cấu trúc dữ liệu có thứ tự
    // tuân theo nguyên tắc FIFO (Faifo) - First In First Out
    // Trong queue, thì có 2 đầu
    // Đầu trước gọi là front - nơi mà ta lấy phần tử ra
    // Đầu sau gọi là rear - nơi mà ta thêm phần tử vào
    // Gia nhập vào queue gọi là enqueue (Thêm phần tử vào cuối hàng đợi)
    // Rời khỏi queue gọi là dequeue (Lấy phần tử ra từ đầu hàng đợi)
    // Phần tử được thêm vào đầu tiên sẽ được lấy ra đầu tiên
    // Tương tự với stack, queue cũng có nhiều loại implementation khác nhau:
    // 1. Simple Circular Array Based
    // 2. Dynamic Circular Array Based
    // 3. Linked List Based
    // Cho dù là loại implementation nào của loại cấu trúc dữ liệu nào thì tụi nó vẫn luon phải tuân thủ theo cái
    // Abstract Data Type (ADT) của loại cấu trúc dữ liệu đó:
    // Queue ADT bao gồm những function sau: enqueue(), dequeue(), front(), size(), isEmptyQueue(),
    // Khi nào nên dùng - when to use:
    // - Thường được sử dụng trong tất cả những thứ phải sếp hàng đợi như: in ấn tài liệu, quản lý tiến trình trong hệ điều hành,

}
